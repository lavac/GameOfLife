public class Cell {
    int xCoordinate;
    int yCoordinate;
    boolean isAlive;

    public Cell(int xCoordinate, int yCoordinate, boolean isAlive) {
        this.xCoordinate = this.xCoordinate;
        this.yCoordinate = yCoordinate;
        this.isAlive = isAlive;
    }

    boolean isAlive() {
        return isAlive;
    }
}
